    private void loadTable () {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NO");
        model.addColumn("ID");
        model.addColumn("Nama Guru");
        model.addColumn("NIP");
        model.addColumn("Pendidikan Terakhir");
        model.addColumn("Gelar");
        model.addColumn("Tanggal Lahir");
        model.addColumn("Tenpat Lahir");
        model.addColumn("Mapel");
        model.addColumn("Alamat");
        model.addColumn("Tahun Masuk");
        model.addColumn("Prestasi");
        model.addColumn("Golongan");
        try {
            int no=1;
            String sql = "select * from guru";
            java.sql.Connection conn=(Connection)config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++, res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7), res.getString(8), res.getString(9), res.getString(10), res.getString(11), res.getString(12)});
            }
            table.setModel(model);
        } catch (Exception e) {
        }
    }

