    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        String search = cariText.getText();
        String sql = "select * from guru where id = '"+search+"' or nip = '"+search+"' or nama_guru = '"+search+"'";
        try {
            java.sql.Connection conn=(Connection)config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                nip.setText(res.getString("nip"));
                namaGuru.setText(res.getString("nama_guru"));
                alamats.setText(res.getString("alamat"));
                pendidikanTerakhir.setSelectedItem(res.getString("pendidikan_terakhir"));
                gelar.setText(res.getString("gelar"));
                tanggalLahir.setDate(null);
                tempatLahir.setText(res.getString("tempat_lahir"));
                mapel.setText(res.getString("mapel"));
                tahunMasuk.setDate(null);
                prestasi.setText(res.getString("prestasi"));
                golongan.setSelectedItem(res.getString("golongan"));
                jenisKelamin.setSelectedItem(res.getString("jenis_kelamin"));
                id.setText(res.getString("id"));
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_cariActionPerformed

